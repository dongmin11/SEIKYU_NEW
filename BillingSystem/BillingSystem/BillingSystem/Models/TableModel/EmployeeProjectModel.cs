using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BillingSystem.Models
{
    /// <summary>
    /// 作業員マスタ
    /// </summary>
    public class EmployeeProjectModel
