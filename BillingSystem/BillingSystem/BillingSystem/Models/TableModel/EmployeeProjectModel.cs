        /// <summary>
        /// /社員ID
        /// </summary>
        public string EmployeeID { get; set; }

        /// <summary>
        /// プロジェクトID
        /// </summary>
        public decimal ProjectID { get; set; }

        /// <summary>
        /// プロジェクト開始日
        /// </summary>
        public DateTime StartDate { get; set; }

        /// <summary>
        /// プロジェクト終了日
        /// </summary>
        public DateTime EndDate { get; set; }

        /// <summary>
        /// 過不足精算フラグ
        /// </summary>
        public int SettlementFlag { get; set; }

        /// <summary>
        /// 過不足精算上限名
        /// </summary>
        public string SettlementUpperLimitName { get; set; }

        /// <summary>
        /// 過不足精算上限
        /// </summary>
        public int SettlementUpperLimit { get; set; }

        /// <summary>
        /// 過不足精算下限名
        /// </summary>
        public string SettlementLowerLimitName { get; set; }

        /// <summary>
        /// 過不足精算下限
        /// </summary>
        public int SettlementLowerLimit { get; set; }

        /// <summary>
        /// 基本月給（円）
        /// </summary>
        public int BasicAmount { get; set; }

        /// <summary>
        /// 備考
        /// </summary>
        public int Note { get; set; }

        /// <summary>
        /// 削除フラグ
        /// </summary>
        public int DeleteFlag { get; set; }

        /// <summary>
        /// LockVer
        /// </summary>
        public int LockVer { get; set; }
    }
}
